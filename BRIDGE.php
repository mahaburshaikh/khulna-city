Khan Jahan Ali Bridge (Bengali: খান জাহান আলী সেতু) is a bridge over Rupsa River in Khulna, Bangladesh. The bridge is also known as Rupsha bridge.
Location
The bridge is located 4.80 km far from Khulna Town. It is called the gateway of Khulna because this bridge connects the Southern districts of Bangladesh with Mongla, the second largest sea port of Bangladesh. The length of the bridge is 1.6 km and its width is 16.48 meters.[1]
